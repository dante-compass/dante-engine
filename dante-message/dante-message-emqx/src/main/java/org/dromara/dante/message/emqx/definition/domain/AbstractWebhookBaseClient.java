package org.dromara.dante.message.emqx.definition.domain;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.google.common.base.MoreObjects;

import java.util.Map;

/**
 * <p>Description: Client 事件基础属性 </p>
 *
 * @author : gengwei.zheng
 * @date : 2023/10/6 22:06
 */
public abstract class AbstractWebhookBaseClient extends AbstractWebhook {

    /**
     * 客户端的 IPAddress 和 Port
     */
    @JsonProperty("peername")
    private String peerName;

    @JsonProperty("client_attrs")
    private Map<String, Object> attributes;

    @JsonProperty("metadata")
    private Map<String, Object> metadata;

    public String getPeerName() {
        return peerName;
    }

    public void setPeerName(String peerName) {
        this.peerName = peerName;
    }

    public Map<String, Object> getAttributes() {
        return attributes;
    }

    public void setAttributes(Map<String, Object> attributes) {
        this.attributes = attributes;
    }

    public Map<String, Object> getMetadata() {
        return metadata;
    }

    public void setMetadata(Map<String, Object> metadata) {
        this.metadata = metadata;
    }

    @Override
    public String toString() {
        return MoreObjects.toStringHelper(this)
                .add("peerName", peerName)
                .toString();
    }
}
